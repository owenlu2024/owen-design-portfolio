function getOptimizedImageSrc(src, variant = "preview") {
  if (!/\.(jpe?g)$/i.test(src) || !src.startsWith("assets/")) {
    return src;
  }

  return src.replace(/^assets\//, `assets/optimized/${variant}/`);
}

function makeImage(src, alt, options = {}) {
  const { loading = "lazy", variant = "preview" } = options;
  const image = document.createElement("img");
  image.src = getOptimizedImageSrc(src, variant);
  image.alt = alt;
  image.loading = loading;
  image.decoding = "async";

  if (loading === "eager") {
    image.fetchPriority = "high";
  }

  image.addEventListener("load", () => image.classList.add("media-loaded"), {
    once: true,
  });
  image.addEventListener(
    "error",
    () => {
      if (image.src !== src) {
        image.src = src;
        return;
      }

      image.classList.add("media-loaded");
    },
    { once: true },
  );
  return image;
}

function makeMedia(src, alt, options = {}) {
  if (src.endsWith(".mp4")) {
    const video = document.createElement("video");
    video.src = src;
    video.muted = true;
    video.loop = true;
    video.autoplay = true;
    video.playsInline = true;
    video.preload = "metadata";
    video.setAttribute("aria-label", alt);
    video.addEventListener("loadeddata", () => video.classList.add("media-loaded"), {
      once: true,
    });
    return video;
  }

  return makeImage(src, alt, options);
}

function setNoWidowText(element, text) {
  element.textContent = text.replace(/\s+(\S+)$/, "\u00A0$1");
}

const revealObserver =
  "IntersectionObserver" in window
    ? new IntersectionObserver(
        (entries) => {
          entries.forEach((entry) => {
            if (entry.isIntersecting) {
              entry.target.classList.add("is-visible");
              revealObserver.unobserve(entry.target);
            }
          });
        },
        {
          threshold: 0.16,
          rootMargin: "0px 0px -8% 0px",
        },
      )
    : null;

function observeReveal(element, delay = 0) {
  element.style.setProperty("--reveal-delay", `${delay}ms`);

  if (!revealObserver) {
    element.classList.add("is-visible");
    return;
  }

  revealObserver.observe(element);
}

let parallaxFrame = null;

function updateDialogParallax() {
  parallaxFrame = null;

  if (!dialog.open) {
    return;
  }

  const maxScroll = Math.max(1, dialog.scrollHeight - dialog.clientHeight);
  const progress = Math.min(1, Math.max(0, dialog.scrollTop / maxScroll));
  detailProgress.style.transform = `scaleY(${progress})`;

  const copyProgress = Math.min(1, dialog.scrollTop / 360);
  dialogCopy.style.opacity = `${1 - copyProgress * 0.72}`;
  dialogCopy.style.transform = `translateY(${-copyProgress * 34}px)`;

  const viewportCenter = dialog.clientHeight / 2;
  dialogGallery.querySelectorAll("img, video").forEach((element) => {
    const rect = element.getBoundingClientRect();
    const elementCenter = rect.top + rect.height / 2;
    const distance = elementCenter - viewportCenter;
    const offset = Math.max(-22, Math.min(22, distance * -0.035));
    const focus = Math.max(0, 1 - Math.abs(distance) / viewportCenter);
    const scale = 1 + focus * 0.018;
    element.style.setProperty("--parallax-y", `${offset.toFixed(2)}px`);
    element.style.setProperty("--focus-scale", scale.toFixed(4));
  });
}

function requestDialogParallaxUpdate() {
  if (parallaxFrame !== null) {
    return;
  }

  parallaxFrame = requestAnimationFrame(updateDialogParallax);
}

function handleTilt(event) {
  if (!window.matchMedia("(hover: hover) and (pointer: fine)").matches) {
    return;
  }

  const card = event.currentTarget;
  const rect = card.getBoundingClientRect();
  const x = (event.clientX - rect.left) / rect.width - 0.5;
  const y = (event.clientY - rect.top) / rect.height - 0.5;
  card.style.setProperty("--tilt-x", `${(-y * 2).toFixed(2)}deg`);
  card.style.setProperty("--tilt-y", `${(x * 2).toFixed(2)}deg`);
}

function resetTilt(event) {
  event.currentTarget.style.setProperty("--tilt-x", "0deg");
  event.currentTarget.style.setProperty("--tilt-y", "0deg");
}

